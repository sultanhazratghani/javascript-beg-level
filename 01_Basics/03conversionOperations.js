// unary operators

// prefix ++a, --a
// postfix a++, a--

// prefix
console.log("prefix ++");
let x=3;
let y=++x; // increments first by one and then assigns value to y variable
console.log(`x:${x}, and y:${y} `);
//------------------------------------------
console.log("prefix --");
let a=3;
let b=--a;
console.log(`a: ${a} and b: ${b}`);
console.log("----------------------");

console.log("postfix ++");
let l=3;
let m=l++;  //assign first then increments by one 
console.log(`l:${l}, and m:${m} `);
//------------------------------------------
console.log("postfix --");
let p=5;
let q=p--;
console.log(`p: ${p} and q: ${q}`);
console.log("----------------------");



